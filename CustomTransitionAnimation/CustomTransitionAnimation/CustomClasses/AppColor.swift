import UIKit

///AppColor Separate class to distinguish color from default native color
public class AppColor {
    
    public static var black: UIColor {
        return UIColor(named: "Black")!
    }
    
    public static var Brown : UIColor {
        return UIColor(named: "Brown")!
        
    }
    
    public static var darkBrown : UIColor {
        return UIColor(named: "DarkBrown")!
        
    }
    
    public static var lightBrown : UIColor {
        return UIColor(named:  "LightBrown")!
    }
    
    public static var white : UIColor {
        return UIColor(named: "White")!
    }
}

